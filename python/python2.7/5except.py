#  IndexError        下标超出序列范围
#  IOError           输入/输出异常;一般是无法打开文件
#  AttributeError    键盘结婚搜到
#  KeyboardInterrupt 键盘接受到Ctrl+C
#  Exception         通用的异常类型;一般会捕捉到所有异常


